   Car_Number VARCHAR(10) NOT NULL,
   Model_Year DATE NOT NULL,
   Mileage NUMERIC NOT NULL,
   Price NUMERIC NOT NULL,
   Color1 VARCHAR(10) NOT NULL,
   Color2 VARCHAR(10),
   Fuel1 VARCHAR(10) NOT NULL,
   Fuel2 VARCHAR(10),
